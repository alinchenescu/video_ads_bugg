import 'package:flutter/material.dart';
import 'package:preload_page_view/preload_page_view.dart';
import 'package:video_player/video_player.dart';
import 'package:visibility_detector/visibility_detector.dart';

class FeedScreen extends StatelessWidget {
  const FeedScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return  PreloadPageView.builder(
      preloadPagesCount: 2,
      scrollDirection: Axis.vertical,
      itemCount: 999,
      controller: PreloadPageController(initialPage: 0),
      itemBuilder: (context, pageIndex) {
        return VideoItemWidget(
          index: pageIndex,
        );
      },
      pageSnapping: true,
    );
  }
}

class VideoItemWidget extends StatefulWidget {
  int index;

   VideoItemWidget({super.key,required this.index});

  @override
  State<VideoItemWidget> createState() => _VideoItemWidgetState();
}

class _VideoItemWidgetState extends State<VideoItemWidget> {
  late VideoPlayerController _controller;
  bool isLoaded = false;
  double visibility = 0;

  @override
  void initState() {
    super.initState();
    _controller = VideoPlayerController.networkUrl(Uri.parse('https://www.elecard.com/storage/video/TSU_1920x1080.mp4'))
    // _controller = VideoPlayerController.networkUrl(Uri.parse(videos[widget.index]))
      ..initialize().then((_) {
        _controller.play();

        _controller.setLooping(true);
        if (visibility >= 0.75) {
          print('Playing ${widget.index}');
          _controller.play();
        }
        setState(() {
          isLoaded = true;
        });
      });
  }

  @override
  Widget build(BuildContext context) {
    return isLoaded == false
        ? Center(
      child: CircularProgressIndicator(),
    )
        : Stack(
      children: <Widget>[
        GestureDetector(
          onLongPressStart: (_) => _controller.pause(),
          onLongPressEnd: (_) => _controller.play(),
          child: Align(
            alignment: AlignmentDirectional.bottomCenter,
            child: SizedBox.expand(
              child: FittedBox(
                  fit: BoxFit.cover,
                  child: Center(
                    child: _controller.value.isInitialized
                        ?
                    VisibilityDetector(
                      key: Key(widget.index.toString()),
                      onVisibilityChanged: (info) {
                        setState(() {
                          visibility = info.visibleFraction;
                        });
                        if (info.visibleFraction == 0 && mounted) {
                          if (_controller.value.isPlaying) {
                            print('pause:${widget.index} ');
                            _controller.pause();
                          }
                        } else if (info.visibleFraction >= 0.75 &&
                            _controller.value.isInitialized) {
                          print('play:${widget.index} ');
                          _controller.play();

                        }
                      },
                      child: SizedBox(
                        width: _controller.value.size.width,
                        height: _controller.value.size.height,
                        child: AspectRatio(
                          aspectRatio:
                          _controller.value.aspectRatio,
                          child: VideoPlayer(
                            _controller,
                          ),
                        ),
                      ),
                    )
                        : CircularProgressIndicator(),
                  )),
            ),
          ),
        ),
      ],
    );
  }

  @override
  void dispose() {
    _controller.dispose();
    print('disposed ${widget.index}');

    super.dispose();
  }
}

